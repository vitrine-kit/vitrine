// Generates kit version constants from package.json files (run on build/typecheck).
import { readFileSync, writeFileSync } from 'node:fs';
import { dirname, join } from 'node:path';
import { fileURLToPath } from 'node:url';

const here = dirname(fileURLToPath(import.meta.url));
const pkgRoot = join(here, '..');
const monorepoRoot = join(pkgRoot, '..', '..');

function pkgVersion(name) {
  const file = join(monorepoRoot, 'packages', name, 'package.json');
  return JSON.parse(readFileSync(file, 'utf8')).version;
}

/** npm caret range that includes the current release (0.x → ^0.{minor}.0, 1.x → ^1.0.0). */
function caretRange(version) {
  const [major, minor] = version.split('.');
  if (major === '0') return `^0.${minor}.0`;
  return `^${major}.0.0`;
}

const kitVersion = pkgVersion('cli');
const coreVersion = pkgVersion('core');
const blueprintVersion = pkgVersion('payload-blueprint');

const out = join(pkgRoot, 'src', 'kit-versions.generated.ts');
writeFileSync(
  out,
  `// Generated by scripts/generate-kit-versions.mjs — do not edit by hand.
export const KIT_VERSION = '${kitVersion}';
/** Contract API semver written to vitrine.json (stable until a breaking contract change). */
export const CONTRACTS_VERSION = '1.0.0';
export const CONTRACTS_RANGE = '^1.0.0';
export const CORE_RANGE = '${caretRange(coreVersion)}';
export const BLUEPRINT_RANGE = '${caretRange(blueprintVersion)}';
`,
);
console.log(`[generate-kit-versions] KIT=${kitVersion} CORE=${caretRange(coreVersion)} BLUEPRINT=${caretRange(blueprintVersion)}`);
